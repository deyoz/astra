    AIRLINE VARCHAR(3) NOT NULL,
    BAG_TYPE VARCHAR(15),
    DOC_AIRCODE VARCHAR(3),
    DOC_COUPON SMALLINT,
    DOC_NO VARCHAR(15) NOT NULL,
    DOC_TYPE VARCHAR(5) NOT NULL,
    DOC_WEIGHT INTEGER,
