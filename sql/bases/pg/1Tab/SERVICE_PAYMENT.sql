    PAX_ID INTEGER,
    POINT_ID INTEGER NOT NULL,
    RFISC VARCHAR(15),
    SERVICE_QUANTITY SMALLINT NOT NULL,
    SERVICE_TYPE VARCHAR(1),
    TRANSFER_NUM SMALLINT NOT NULL
);

