CREATE TABLE POINTS (
    ACT_IN TIMESTAMP(0),
    ACT_OUT TIMESTAMP(0),
    AIRLINE VARCHAR(3),
    AIRLINE_FMT SMALLINT,
    AIRP VARCHAR(3) NOT NULL,
    AIRP_FMT SMALLINT NOT NULL,
    BORT VARCHAR(10),
    CRAFT VARCHAR(3),
    CRAFT_FMT SMALLINT,
    EST_IN TIMESTAMP(0),
    EST_OUT TIMESTAMP(0),
    FIRST_POINT INTEGER,
    FLT_NO INTEGER,
    LITERA VARCHAR(3),
    MOVE_ID INTEGER NOT NULL,
    PARK_IN VARCHAR(3),
    PARK_OUT VARCHAR(3),
    POINT_ID INTEGER NOT NULL,
    POINT_NUM INTEGER NOT NULL,
    PR_DEL SMALLINT DEFAULT 0 NOT NULL,
    PR_REG SMALLINT NOT NULL,
    PR_TRANZIT SMALLINT NOT NULL,
    REMARK VARCHAR(250),
    SCD_IN TIMESTAMP(0),
    SCD_OUT TIMESTAMP(0),
    SUFFIX VARCHAR(1),
    SUFFIX_FMT SMALLINT,
    TID INTEGER NOT NULL,
    TIME_IN TIMESTAMP(0),
    TIME_OUT TIMESTAMP(0),
    TRIP_TYPE VARCHAR(1)
);

