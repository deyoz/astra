    POINT_ID NUMBER(9) NOT NULL,
    SEATS NUMBER(1) NOT NULL,
    STATUS VARCHAR2(20) NOT NULL,
    SUBCLS VARCHAR2(1) NOT NULL,
    SURNAME VARCHAR2(64) NOT NULL
);
