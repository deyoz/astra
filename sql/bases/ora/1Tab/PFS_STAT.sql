CREATE TABLE PFS_STAT (
    AIRP_ARV VARCHAR2(3) NOT NULL,
    BIRTH_DATE DATE,
    GENDER VARCHAR2(2),
    NAME VARCHAR2(64),
    PAX_ID NUMBER(9) NOT NULL,
