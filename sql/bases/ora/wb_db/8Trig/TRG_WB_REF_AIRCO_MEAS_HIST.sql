AFTER
INSERT OR UPDATE
ON WB_REF_AIRCO_MEAS
FOR EACH ROW
BEGIN
  if inserting then
    insert into WB_REF_AIRCO_MEAS_HIST (ID_,
	                                        U_NAME_,
	                                          U_IP_,
	                                            U_HOST_NAME_,
	                                              DATE_WRITE_,
	                                                ACTION_,
	                                                  ID,
	                                                    ID_AC_OLD,
	                                                      DATE_FROM_OLD,
