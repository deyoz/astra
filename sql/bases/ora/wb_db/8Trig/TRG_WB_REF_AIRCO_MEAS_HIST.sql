                                                 :new.WEIGHT_ID,
	                                                 :new.VOLUME_ID,
	                                                   :new.LENGTH_ID,
	                                                     :new.DENSITY_ID,
	                                                       :new.U_NAME,
	                                                         :new.U_IP,
	                                                           :new.U_HOST_NAME,
	                                                             :new.DATE_WRITE
    from dual;
  end if;
